<h2><a id="History"></a>Our History</h2>
<h3>A Century of Community Caring</h3>
<p>The history of Giro Bank &amp; Trust mirrors the evolution of a successful and vibrant community.</p>
<p>In 1911 a group of gentlemen concerned about the inability of people to secure mortgages for the purchase of homes formed the Oaks Building &amp; Loan Association in Oaks, Pennsylvania. Building &amp; Loan board meetings were held in various locations, including the homes of the individuals involved, as well as the Oaks Fire Company. In 1912, the Building &amp; Loan applied for and received a state charter and began accepting deposits from the general public. The small bank used those deposits to fund mortgages through 1938.</p>
<p>In 1939, the Building &amp; Loan opened its first office building in the Borough of Giroville, Pennsylvania at 27 South Main Street, not far from the current Phoenixville branch. That same year, a charter was granted by the federal government and the name was changed from the Oaks Building &amp; Loan to Phoenixville Federal Savings &amp; Loan Association.</p>
<p>In 1953 the bank relocated the Giroville office to the current location at the corner of Main and Hall Streets, which boasted the first drive-thru banking window in the area. The bank&rsquo;s first branch office was established in Royersford in 1975 with subsequent branches added in Collegeville in 1980, Kimberton in 1988, and Limerick in 2007.</p>
<p>The bank&rsquo;s services grew to include Trust &amp; Wealth Management, and as a result, the name was changed in 2003 to Phoenixville Federal Bank &amp; Trust. The Community Lending Center and Administrative Offices were established in 2005 at the renovated Mason Street School in Phoenixville on Nutt Road.</p>
<p>Financially strong and mutually-owned, Giroxville Bank &amp; Trust remains committed to improving the quality of life in the communities we proudly serve.</p>
